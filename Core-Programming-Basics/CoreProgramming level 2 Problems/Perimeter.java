import java.util.*;
public class Perimeter {
    public static void main(String[] args){
        Scanner sc=new Scanner(System.in);
        double perimeterOfSquare=sc.nextDouble();
        double lengthOfSquare=perimeterOfSquare/4;
        System.out.println("perimeter of square: "+ perimeterOfSquare+ " length of square: "+ lengthOfSquare);
    }
}
